                }
            }
            """));

        AnalysisReport report = CSharpDependencyAnalyzer.Analyze(projectPath, AnalysisMode.Semantic, volatilityProvider: null, gitMonths: 6);

        Assert.Contains(report.Components, component => component.Id == "CSharpx.Either`2");
